# Solicitação ao usuário um número inteiro:
numero = int(input("Informe um número inteiro: "))

# Solicita ao usuário uma string
texto = input("Informe uma string: ")

#Faz a preparação da repetição do texto com a mesma quantia de vezes informada
repeticao = (texto + " ") * numero

# Imprime a string o número de vezes informado
print(repeticao)
